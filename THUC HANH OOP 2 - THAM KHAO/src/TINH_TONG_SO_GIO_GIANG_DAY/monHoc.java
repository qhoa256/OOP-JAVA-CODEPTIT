package TINH_TONG_SO_GIO_GIANG_DAY;

public class monHoc {
    private String maMon, tenMon;

    public monHoc(String maMon, String tenMon) {
        this.maMon = maMon;
        this.tenMon = tenMon;
    }

    public String getMaMon() {
        return maMon;
    }

    public String getTenMon() {
        return tenMon;
    }
}
